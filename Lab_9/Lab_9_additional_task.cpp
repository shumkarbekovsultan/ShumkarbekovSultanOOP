template<typename T, class T2>
class ArrayUtility {
public:
    static T findMax(const T arr[], int size , T2 mul) {
        T maxElement = std::numeric_limits<T>::min();
        for (int i = 0; i < size; ++i) {
            if (arr[i] * mul > maxElement) {
                maxElement = arr[i] * mul;
            }
        }
        return maxElement;
    }

    static void sortArray(T arr[], int size) {
        sort(arr, arr + size);
    }
};

